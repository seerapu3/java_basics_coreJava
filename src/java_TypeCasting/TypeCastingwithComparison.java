package java_TypeCasting;

public class TypeCastingwithComparison {
	
	public static void main(String[] args) {
		int num1 = 5;
		double num2 = 5.0;
		boolean isEqual = num1 == (int) num2; // Explicit type casting for comparison
		System.out.println("Is equal? " + isEqual); // Output: Is equal? true

	}
}
