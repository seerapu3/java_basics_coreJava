package java_TypeCasting;

public class TypeCastingbetweenCharandInt {
	
	public static void main(String[] args) {
		char ch = 'A';
		int asciiValue = (int) ch; // Explicit type casting to get the ASCII value of the character
		System.out.println("ASCII value: " + asciiValue); // Output: ASCII value: 65

	}
}
