package java_typeCasting;

public class TypeCastingwithArithmeticOperations {
	
	public static void main(String[] args) {
		int num1 = 10;
		int num2 = 3;
		double result = (double) num1 / num2; // Explicit type casting to perform division with double precision
		System.out.println("Result: " + result); // Output: Result: 3.3333333333333335

	}
}
