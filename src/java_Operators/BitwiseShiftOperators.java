package java_Operators;
public class BitwiseShiftOperators {
    public static void main(String[] args) {
        int num = 15;
        System.out.println("num: " + num);
        System.out.println("num << 2: " + (num << 2));
        System.out.println("num >> 2: " + (num >> 2));
        System.out.println("num >>> 2: " + (num >>> 2));
    }
}
