package java_DataTypes;
public class ObjectDataType {
    public static void main(String[] args) {
        Object data = "Hello, Java!";
        System.out.println("Data: " + data);

        data = 10;
        System.out.println("Data: " + data);
        
        data = true;
        System.out.println("Data: " + data);
        
    }
}
